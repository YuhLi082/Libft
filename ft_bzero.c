#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*i;
	size_t			t;

	i = (unsigned char *)s;
	t = 0;
	while (t < n)
	{
		i[t] = 0;
		t++;
	}
}
